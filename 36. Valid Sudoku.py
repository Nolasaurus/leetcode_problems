class Solution:
    def isValidSudoku(self, board: list[list[str]]) -> bool:
        centers = [1,4,7]

        for i in range(9):
            seen_x = []
            seen_y = []
            for j in range(9):
                #check rows 
                if board[i][j] != ".": 
                    if board[i][j] in seen_x:
                        return False
                    else:
                        seen_x.append(board[i][j])

                #check columns
                if board[j][i] != ".":
                    if board[j][i] in seen_y: 
                        return False
                    else:
                        seen_y.append(board[j][i])


                #check all around square
                if i in centers and j in centers:
                    seen_33 = []
                    for x in [-1,0,1]:
                        for y in [-1,0,1]:
                            if board[i-x][j-y] != ".":
                                if board[i-x][j-y] in seen_33:
                                    return False
                                else:
                                    seen_33.append(board[i-x][j-y])

                #check columns


        return True

